// 俯仰角查表表：适配0~1.5f相机高度，输出合理pitch弧度
// 索引对应高度档位，数值为最优俯仰角
static const float pitch_table[16] = {
    -0.15f, -0.10f, -0.05f, 0.00f,
     0.05f,  0.10f,  0.15f,  0.20f,
     0.25f,  0.30f,  0.35f,  0.40f,
     0.45f,  0.50f,  0.55f,  0.60f
};

// 快速cos查表，角度范围不限（自动归一化0~359）
float fast_cos(int angle)
{
    angle %= 360;
    if(angle < 0) angle += 360;
    return cos_table[angle];
}

// 快速sin查表：sin(x) = cos(x-90°)
float fast_sin(int angle)
{
    return fast_cos(angle - 90);
}

// 高度适配俯仰角：无数学库，纯查表插值
// 相机越高，pitch越大（低头俯视）；相机越低，pitch越小（抬头仰视）
float fast_pitch(float cam_y)
{
    // 限制高度区间，防止角度溢出
    if(cam_y < -0.2f) cam_y = -0.2f;
    if(cam_y > 1.4f) cam_y = 1.4f;

    // 档位映射：高度[-0.2,1.4] 映射到 0~15查表索引
    int idx = (int)((cam_y + 0.2f) / 1.6f * 15.0f);
    if(idx < 0) idx = 0;
    if(idx > 15) idx = 15;

    return pitch_table[idx];
}
